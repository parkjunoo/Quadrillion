import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const PLAYER_INPUT_FILE = path.resolve('data/world-cup-squad-market-values/world_cup_squad_player_market_values.csv');
const COUNTRY_TOP5_INPUT_FILE = path.resolve(
  'data/world-cup-squad-market-values/world_cup_country_squad_market_values_top5_video.csv',
);
const GENERATED_SOURCE_DIR = path.resolve('src/projects/world-cup-squad-market-values/generated');
const GENERATED_OUTPUT_FILE = path.join(GENERATED_SOURCE_DIR, 'worldCupSquadPlayerBreakdowns.ts');
const TOP_PLAYER_LIMIT = 6;

const main = async () => {
  await mkdir(GENERATED_SOURCE_DIR, { recursive: true });

  const [playerRows, countryRows] = await Promise.all([
    readCsv(PLAYER_INPUT_FILE),
    readCsv(COUNTRY_TOP5_INPUT_FILE),
  ]);
  const playersByCountryYear = groupBy(playerRows, (row) => `${row.year}\u0000${row.country}`);
  const breakdowns = countryRows
    .map((countryRow) => {
      const year = Number(countryRow.year);
      const country = countryRow.name;
      const totalValue = Number(countryRow.value) || 0;
      const squadRows = playersByCountryYear.get(`${year}\u0000${country}`) ?? [];
      const knownPlayers = squadRows
        .filter((row) => Number(row.value_million_eur) > 0)
        .slice()
        .sort(
          (rowA, rowB) =>
            Number(rowB.value_million_eur) - Number(rowA.value_million_eur) ||
            rowA.player_name.localeCompare(rowB.player_name),
        );
      const topPlayers = knownPlayers.slice(0, TOP_PLAYER_LIMIT).map((row) => ({
        name: row.player_name,
        position: row.position,
        value: roundMillionValue(row.value_million_eur),
      }));
      const topPlayerValue = topPlayers.reduce((sum, player) => sum + player.value, 0);
      const restValue = Math.max(0, roundMillionValue(totalValue - topPlayerValue));

      return {
        code: countryRow.code,
        country,
        knownPlayerCount: knownPlayers.length,
        playerCount: Number(countryRow.playerCount) || squadRows.length,
        players: topPlayers,
        restKnownPlayerCount: Math.max(0, knownPlayers.length - topPlayers.length),
        restPlayerCount: Math.max(0, squadRows.length - topPlayers.length),
        restValue,
        totalValue,
        year,
      };
    })
    .sort((rowA, rowB) => rowA.year - rowB.year || rowA.country.localeCompare(rowB.country));

  await writeFile(GENERATED_OUTPUT_FILE, generatedBreakdownModule(breakdowns));
  console.log(`Generated ${path.relative(process.cwd(), GENERATED_OUTPUT_FILE)} (${breakdowns.length} country-years)`);
};

const readCsv = async (filePath) => parseCsv(await readFile(filePath, 'utf8'));

const parseCsv = (csv) => {
  const lines = csv.trim().split(/\r?\n/).filter(Boolean);
  const [headerLine, ...bodyLines] = lines;
  const headers = splitCsvLine(headerLine);

  return bodyLines.map((line) => {
    const columns = splitCsvLine(line);
    const row = {};

    headers.forEach((header, index) => {
      row[header] = columns[index] ?? '';
    });

    return row;
  });
};

const splitCsvLine = (line) => {
  const columns = [];
  let current = '';
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const nextChar = line[index + 1];

    if (char === '"' && inQuotes && nextChar === '"') {
      current += '"';
      index += 1;
      continue;
    }

    if (char === '"') {
      inQuotes = !inQuotes;
      continue;
    }

    if (char === ',' && !inQuotes) {
      columns.push(current);
      current = '';
      continue;
    }

    current += char;
  }

  columns.push(current);
  return columns;
};

const generatedBreakdownModule = (breakdowns) => [
  '// Generated by scripts/build-world-cup-squad-player-breakdowns.mjs. Do not edit manually.',
  '',
  'export type WorldCupSquadPlayerSegment = {',
  '  name: string;',
  '  position: string;',
  '  value: number;',
  '};',
  '',
  'export type WorldCupSquadPlayerBreakdown = {',
  '  code: string;',
  '  country: string;',
  '  knownPlayerCount: number;',
  '  playerCount: number;',
  '  players: readonly WorldCupSquadPlayerSegment[];',
  '  restKnownPlayerCount: number;',
  '  restPlayerCount: number;',
  '  restValue: number;',
  '  totalValue: number;',
  '  year: number;',
  '};',
  '',
  `export const worldCupSquadPlayerBreakdowns = ${JSON.stringify(breakdowns, null, 2)} as const satisfies readonly WorldCupSquadPlayerBreakdown[];`,
  '',
].join('\n');

const roundMillionValue = (value) => Math.round(Number(value) * 10) / 10;

const groupBy = (rows, keyFn) => {
  const grouped = new Map();

  for (const row of rows) {
    const key = keyFn(row);
    const group = grouped.get(key) ?? [];

    group.push(row);
    grouped.set(key, group);
  }

  return grouped;
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
