import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const DATA_DIR = path.resolve('data/youtube-subscriber-race');
const GENERATED_SOURCE_DIR = path.resolve('src/projects/youtube-subscriber-race/generated');
const SOURCE_FILE = path.join(DATA_DIR, 'youtube_subscribers_annual_2016_2026_draft.csv');
const VIDEO_CSV_FILE = path.join(DATA_DIR, 'youtube_subscribers_annual_top10_video.csv');
const GENERATED_TS_FILE = path.join(GENERATED_SOURCE_DIR, 'youtubeSubscriberRaceCsv.ts');

const VIDEO_HEADERS = [
  'date',
  'year',
  'quarter',
  'month',
  'name',
  'code',
  'region',
  'value',
  'rank',
  'color',
  'source',
  'confidence',
  'notes',
];

const main = async () => {
  await mkdir(DATA_DIR, { recursive: true });
  await mkdir(GENERATED_SOURCE_DIR, { recursive: true });

  const rows = (await readCsv(SOURCE_FILE))
    .filter((row) => row.include_by_default === 'yes')
    .map((row) => ({
      ...row,
      value: Number(row.value),
      year: Number(row.year),
      month: Number(row.month),
      quarter: Number(row.quarter),
    }));
  const rankedRows = rankRowsByDate(rows)
    .sort((a, b) => a.date.localeCompare(b.date) || Number(a.rank) - Number(b.rank))
    .map((row) => ({
      date: row.date,
      year: row.year,
      quarter: row.quarter,
      month: row.month,
      name: row.name,
      code: row.code,
      region: row.region,
      value: row.value,
      rank: row.rank,
      color: row.color,
      source: row.source,
      confidence: row.confidence,
      notes: row.notes,
    }));

  await writeCsv(VIDEO_CSV_FILE, rankedRows, VIDEO_HEADERS);
  await writeFile(
    GENERATED_TS_FILE,
    generatedCsvModule(rankedRows, VIDEO_HEADERS),
  );

  console.log(`Wrote ${rankedRows.length} video rows to ${VIDEO_CSV_FILE}`);
  console.log(`Wrote generated module to ${GENERATED_TS_FILE}`);
};

const rankRowsByDate = (rows) => {
  const rowsByDate = groupBy(rows, (row) => row.date);

  return [...rowsByDate.values()].flatMap((dateRows) =>
    dateRows
      .toSorted((a, b) => b.value - a.value || a.name.localeCompare(b.name))
      .map((row, index) => ({
        ...row,
        rank: index + 1,
      }))
  );
};

const groupBy = (items, keyFor) => {
  const groups = new Map();

  for (const item of items) {
    const key = keyFor(item);
    const group = groups.get(key) ?? [];
    group.push(item);
    groups.set(key, group);
  }

  return groups;
};

const readCsv = async (filePath) => parseCsv(await readFile(filePath, 'utf8'));

const parseCsv = (csv) => {
  const [headerLine, ...bodyLines] = csv.trim().split(/\r?\n/).filter(Boolean);
  const headers = splitCsvLine(headerLine);

  return bodyLines.map((line) => {
    const columns = splitCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, columns[index] ?? '']));
  });
};

const writeCsv = async (filePath, rows, headers) => {
  const body = rows.map((row) => headers.map((header) => csvValue(row[header])).join(','));
  await writeFile(filePath, `${headers.join(',')}\n${body.join('\n')}\n`);
};

const generatedCsvModule = (rows, headers) => {
  const csv = [
    headers.join(','),
    ...rows.map((row) => headers.map((header) => csvValue(row[header])).join(',')),
  ].join('\n');

  return [
    '// Generated by scripts/build-youtube-subscriber-race.mjs. Do not edit manually.',
    'export const youtubeSubscriberRaceCsv =',
    `  ${JSON.stringify(`${csv}\n`)};`,
    '',
  ].join('\n');
};

const splitCsvLine = (line) => {
  const columns = [];
  let current = '';
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const nextChar = line[index + 1];

    if (char === '"' && inQuotes && nextChar === '"') {
      current += '"';
      index += 1;
      continue;
    }

    if (char === '"') {
      inQuotes = !inQuotes;
      continue;
    }

    if (char === ',' && !inQuotes) {
      columns.push(current);
      current = '';
      continue;
    }

    current += char;
  }

  columns.push(current);
  return columns;
};

const csvValue = (value) => {
  const text = String(value ?? '');
  return /[",\n\r]/.test(text) ? `"${text.replaceAll('"', '""')}"` : text;
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
