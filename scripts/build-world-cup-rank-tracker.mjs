import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const INPUT_FILE = path.resolve('data/world-cup-standings/world_cup_top16_standings.csv');
const OUTPUT_FILE = path.resolve(
  'src/projects/world-cup-rank-tracker/generated/worldCupRankSnapshots.ts',
);
const rankCutoff = 8;

const csvText = await readFile(INPUT_FILE, 'utf8');
const rows = parseCsv(csvText);
const selectedYears = [...new Set(rows.map((row) => Number(row.year)))]
  .filter((year) => Number.isFinite(year))
  .sort((yearA, yearB) => yearA - yearB);
const finalTournamentYear = selectedYears[selectedYears.length - 1];
const selectedCountries = [];
const selectedCountryNames = new Set();

for (const row of rows
  .filter((entry) => Number(entry.final_rank) <= rankCutoff)
  .sort((rowA, rowB) =>
    Number(rowA.year) - Number(rowB.year) ||
    Number(rowA.final_rank) - Number(rowB.final_rank) ||
    rowA.team.localeCompare(rowB.team),
  )) {
  if (selectedCountryNames.has(row.team)) {
    continue;
  }

  selectedCountryNames.add(row.team);
  selectedCountries.push({
    code: row.team_code,
    color: colorForIndex(selectedCountries.length),
    id: slugify(row.team),
    name: row.team,
  });
}

const selectedCountryByName = new Map(selectedCountries.map((country) => [country.name, country]));
const rankByYearAndCountry = new Map(
  rows.map((row) => [`${row.year}:${row.team}`, row]),
);

const snapshots = selectedYears.map((year) => {
  const yearRows = rows.filter((row) => Number(row.year) === year);
  const host = yearRows[0]?.host ?? '';

  return {
    year,
    label: `${year} ${host}`,
    host,
    countries: selectedCountries.map((country) => {
      const rankingRow = rankByYearAndCountry.get(`${year}:${country.name}`);
      const finalRank = rankingRow ? Number(rankingRow.final_rank) : null;
      const rank = finalRank && finalRank <= rankCutoff ? finalRank : null;

      return {
        ...selectedCountryByName.get(country.name),
        rank,
        stage: rank ? rankingRow?.stage ?? 'top_8' : 'outside_top_8',
      };
    }),
  };
});

for (const snapshot of snapshots) {
  const rankedCountries = snapshot.countries
    .filter((country) => country.rank !== null)
    .sort((countryA, countryB) => countryA.rank - countryB.rank);
  const rankList = rankedCountries.map((country) => country.rank).join(',');
  const expectedRankList = Array.from({ length: rankCutoff }, (_, index) => index + 1).join(',');

  if (rankList !== expectedRankList) {
    throw new Error(
      `${snapshot.label} is missing top ${rankCutoff} ranks. Found ranks: ${rankList || 'none'}`,
    );
  }
}

await mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
await writeFile(
  OUTPUT_FILE,
  `// Generated by scripts/build-world-cup-rank-tracker.mjs. Do not edit manually.\n` +
    `import type { WorldCupRankSnapshot } from '../rankData';\n\n` +
    `export const worldCupRankSnapshots: WorldCupRankSnapshot[] = ${JSON.stringify(snapshots, null, 2)};\n`,
  'utf8',
);

console.log(`Wrote ${OUTPUT_FILE}`);
console.log(
  `Using ${selectedYears.length} tournaments (${selectedYears[0]}-${finalTournamentYear}), ${selectedCountries.length} countries, top ${rankCutoff} only.`,
);
for (const snapshot of snapshots) {
  const summary = snapshot.countries
    .map((country) => `${country.name}:${country.rank ?? 'OUT'}`)
    .join(', ');
  console.log(`${snapshot.label} - ${summary}`);
}

function parseCsv(text) {
  const lines = text.trim().split(/\r?\n/);
  const headers = parseCsvLine(lines.shift() ?? '');

  return lines.map((line) => {
    const values = parseCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, values[index] ?? '']));
  });
}

function parseCsvLine(line) {
  const values = [];
  let current = '';
  let quoted = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];
    const nextChar = line[index + 1];

    if (char === '"' && quoted && nextChar === '"') {
      current += '"';
      index += 1;
      continue;
    }

    if (char === '"') {
      quoted = !quoted;
      continue;
    }

    if (char === ',' && !quoted) {
      values.push(current);
      current = '';
      continue;
    }

    current += char;
  }

  values.push(current);

  return values;
}

function colorForIndex(index) {
  const hue = (index * 137.508 + 48) % 360;
  return hslToHex(hue, 86, 60);
}

function hslToHex(hue, saturation, lightness) {
  const s = saturation / 100;
  const l = lightness / 100;
  const chroma = (1 - Math.abs(2 * l - 1)) * s;
  const huePrime = hue / 60;
  const x = chroma * (1 - Math.abs((huePrime % 2) - 1));
  const [red1, green1, blue1] =
    huePrime < 1 ? [chroma, x, 0] :
    huePrime < 2 ? [x, chroma, 0] :
    huePrime < 3 ? [0, chroma, x] :
    huePrime < 4 ? [0, x, chroma] :
    huePrime < 5 ? [x, 0, chroma] :
    [chroma, 0, x];
  const match = l - chroma / 2;

  return `#${[red1, green1, blue1]
    .map((value) => Math.round((value + match) * 255).toString(16).padStart(2, '0'))
    .join('')}`;
}

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}
